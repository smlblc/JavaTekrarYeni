package gun15;

public class Task1 {
    // q1
    // print grid of "#" with 10 rows and 10 columns
    // using nested loop
    /*
    #
     */


    public static void main(String[] args) {

        for(int outer = 0; outer < 10; outer++) {

            for(int inner = 0; inner < 10; inner++) {
                System.out.print( "#" );
            }
            System.out.println();

        }


    }


}
