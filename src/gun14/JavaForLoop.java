package gun14;

public class JavaForLoop {

    public static void main(String[] args) {
        //while
        int i = 0;
        while(i < 5) {
            System.out.println( "while: " + i );
            i++;
        }


        //for
        for(int x = 0; x < 5; x++) {
            System.out.println( "for: " + x );
        }

    }
}
