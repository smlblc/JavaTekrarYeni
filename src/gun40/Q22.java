package gun40;


//this is class
public class Q22 {
    //instance variables
    public String name;

    //constructors
    public Q22(String name) {
        this.name = name;
    }

    //instance methods
    public String getName() {
        return name;
    }

    public String toString() {
        return "Q22{" +
                "name='" + getName() + '\'' +
                '}';
    }

    //class methods(static methods)

}
